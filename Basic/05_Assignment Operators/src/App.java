/* 
 *  = , +=, -=, *=, /=, %= Assignment Operators
 */
public class App {
	
	public static void main(String[] args) {
		
		int x = 4;
		int y = 6;
		
		x = y;
		System.out.println(x);
		
		x += 5;
		System.out.println(x); // x = x + 5;
		
		x -= 3;
		System.out.println(x);
		
		x *= 2;
		System.out.println(x);
		
		x /= 3;
		System.out.println(x);
		
		x %= 3;
		System.out.println(x);
		
		
		
	}

}
