/*
 * ==, !=, >, <, >=, <=, Relational Operator
 */
public class App {
	
	public static void main(String[] args) {
		int x = 2*3;
		int y = 3*2;
		boolean a = 3 > 2;
		boolean b = 5 < 2;
		boolean c = 4 >= 4;
		boolean d = 6 <= 9;
		boolean e = 3 == 4;
		boolean f = 5 != 4;
		boolean g = x == y;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(e);
		System.out.println(f);
		System.out.println(g);
	}
}
